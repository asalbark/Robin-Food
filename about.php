At the event, a scrumptuous menu was served to the general public using the pay-as-you-wish method, in which all the dishes were based on fruits and vegetables resced from being unjustly wasted. 
The event was organized by a culinary-activist group that called itself 'Robin Food'. 
The idea was to save food, even if it's a drop in the ocean of waste food, to raise awareness of a critical problem that doesn't get the attention it deserves - the problem of food waste. 
When we realized that almost half of the food waste in Israel takes place in private households - it was clear that the group needed to initiate a change there, and help people 'save their own food'! 
The change should start by reaching people's hearts - through their stomachs, in a delicious and enlightening experience. 
These events quickly grew and became mini-festivals, with hundreds of guests from all over the country, food markets, musical performances, guest lectures and more. 
The events were attended by students, families, retirees, bourgeois and homeless alike, and all enjoyed the untapped resource: good, healthy and tasty food. 
Between events, the group would regularly rescue food setting up neighborhood food sharing booths and inviting people to enjoy the abundance of fruits and vegetables that were salvaged. 
The reputation of Robin Food spread beyond the borders of Haifa, and invitations came from all over the country for lectures, catering, workshops and cooking competitions. All of these helped Robin Food to bring the message to more audiences and generate income for the association, which was necessary to expand its activity and impact.
