    var isHebrew = true; // Flag to track language state

    // Function to toggle translation between Hebrew and English
    function toggleTranslation() {
        var elementsToTranslate = document.querySelectorAll('[data-he]');
        elementsToTranslate.forEach(function(element) {
            if (isHebrew) {
                element.textContent = element.getAttribute('data-en');
            } else {
                element.textContent = element.getAttribute('data-he');
            }
        });
        isHebrew = !isHebrew; // Toggle language state
    }
